#!/bin/bash

# A variable is like a container to store data (text, numbers, etc.)
# You assign a value to a variable like this:

name="im-faix"

# IMPORTANT: No spaces around the equal sign (=)

# To use (reference) a variable, add a dollar sign ($) in front of its name

echo "Welcome, $name!"  # This will print: Welcome, im-faix!
